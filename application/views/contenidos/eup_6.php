<h1>Clasificación por tipo de metodología: Secuencial- lineal- incremental, iterativa, ágil</h1>
<div class ="lead">
<p class ="lead">
    Según fuentes de estudio EUP es una metodología ágil por ser una extensión de Rational Unified 
    Process(RUP) pero más enfocado a las necesidades y informacion operativa de la empresa y no
    tanto como RUP que se enfoca  más en desarrollo y entrega de software 
    sin prestar atención a procesos posteriores a la entrega del software desarrollado, 
    por lo tanto EUP cubre estos objetivos al dar soluciones durante las etapas de 
    producción y retiro software. Asi presta atencion a los datos obtenidos cuando el software 
    está en proceso de produccion o es retirado por no cumplir las espectativas.  
</p>

<p class ="lead">Si bien existe un enfoque importante en las metodologías de desarrollo de software como 
    Scrum y XP, y mejor aún en los marcos de entrega de soluciones tales como 
    <a href="http://disciplinedagiledelivery.com/"> Disciplined Agile Delivery (DAD)</a> , 
    está claro que hay más en los departamentos de tecnología de la información (IT) que en el
     desarrollo / entrega. Lo que realmente necesitamos es un proceso de software completo.</p>

