  <h2>Proceso Unificado Racional</h2> 
            <p class ="lead">El Proceso Racional Unificado o RUP (por sus siglas en inglés de Rational 
                Unified Process) es un proceso de desarrollo de
            software desarrollado por la empresa Rational Software, 
            actualmente propiedad de IBM.1​ Junto con el Lenguaje Unificado de
            Modelado (UML), constituye la metodología estándar más utilizada para el análisis,
             diseño, implementación y documentación
            de sistemas orientados a objetos.</p>
             <p class ="lead">El RUP no es un sistema con pasos firmemente establecidos, sino un conjunto 
                 de metodologías adaptables al contexto y necesidades
            de cada organización. También se conoce por este nombre al software, también 
            desarrollado por Rational, que incluye información
            entrelazada de diversos artefactos y descripciones de las diversas actividades. 
            Está incluido en el Rational Method Composer
            (RMC), que permite la personalización de acuerdo con las necesidades. 
            Originalmente se diseñó un proceso genérico y de dominio
            público, el Proceso Unificado, y una especificación más detallada, 
            el Rational Unified Process, que se vendiera como producto
            independiente.</p>
             <h3>Normalmente se describe desde 3 perspectivas:</h3> 
             <ul>
                <li>Una perspectiva dinámica que muestra las fases en el tiempo;</li>
                <li>Una perspectiva estática que muestra las actividad es del proceso; </li>
                <li>Una perspectiva práctica que sugiere buenas prácticas.</li>
             </ul>
               <h3> Fases RUP</h3> 
               <ul>
                    <li>Concepción – Establecer el modelo comercial para el sistema.</li>
                    <li>Elaboración – Desarrollar un entendimiento del dominio</li>
                    <li>el problema y la arquitectura del sistema.</li>
                    <li>Construcción – Diseño de sistemas, programación y pruebas. </li>
                    <li>Transición - Distribuir el sistema a los usuarios finales en su entorno operativo.</li>
               </ul>
               <h3>Buenas prácticas RUP</h3>
                <ul>
                    <li>Desarrollar software iterativamente </li>
                    <li>Obtener, organizar y gestionar los cambios de requisitos </li>
                    <li>Uso de arquitecturas basadas en componentes </li>
                    <li>Modelar visualmente el software usando UML </li>
                    <li>Verificar de forma continua la calidad del software</li>
                    <li>Control de cambios en el software</li>
                </ul>  
