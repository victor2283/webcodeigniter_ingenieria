            <h3 class ="text-danger">1- Gestión de la Integración del Proyecto</h3>
            <p class ="lead">
            Esta área de conocimiento define procesos y actividades del proyecto. Se debe identificar las necesidades de los partes interesadas(<a href="https://www.significados.com/stakeholder/">stakeholders</a>)
            que se relaciona en el proyecto. Además se trata de definir el alcance, roles, visión, misión, etc, que se encuentran involucrados.
            También, se procede a combinar y unificar los procesos y actividades de dirección de proyectos. Finalmente, se combinan todos
            los aspectos mencionados para lograr un eficiente desarrollo del proyecto.
            </p>
            <p class ="lead">
            Para el desarrollo de un proyecto, la integración abarca características de consolidación, dirección, conjunción y factores
            de integración que son necesarios para finalizarlo, utilizando los recursos y tiempo disponibles.
            </p>
            <p class ="lead">
            Para esto, la integración implica saber cómo reunir los recursos, como distribuirlos correctamente y como hacer efectivos
            sus tiempos; adelantándose a posibles problemas que interrumpan el normal flujo de desarrollo.
            </p>
            <p class ="lead">
            Así, la integración está fuertemente vinculada con la relación que tienen los procesos entre los grupos de procesos del
            directorio, que son la guía para alcanzar los objetivos del proyecto definidos por la organización. “Según el <a class ="text-danger lead" href="https://www.esan.edu.pe/apuntes-empresariales/2018/10/la-importancia-del-pmbok-y-su-influencia-en-un-proyecto/"> PMBOK</a>, los
            procesos de integración de dirección de proyectos incluyen:
            </p>
            <ul>
            <li>
                <strong>Desarrollar el Acta de Constitución del Proyecto:</strong> desarrollar el acta de constitución del proyecto que autoriza
                formalmente un proyecto o una fase de un proyecto. </li>
            <li>
            <strong>Desarrollar el Enunciado del Alcance del Proyecto (Preliminar):</strong> desarrollar el enunciado del alcance del proyecto
                preliminar que ofrece una descripción del alcance de alto nivel. </li>
            <li>
            <strong>Desarrollar el Plan de Gestión del Proyecto:</strong> documentar las acciones necesarias para definir, preparar, integrar
                y coordinar todos los planes subsidiarios en un plan de gestión del proyecto. </li>
            <li>
            <strong>Dirigir y Gestionar la Ejecución del Proyecto:</strong> ejecutar el trabajo definido en el plan de gestión del proyecto
                para lograr los requisitos del proyecto definidos en el enunciado del alcance del proyecto.</li>

            <li>
                <strong>Supervisar y Controlar el Trabajo del Proyecto:</strong> supervisar y controlar los procesos requeridos para iniciar,
                planificar, ejecutar y cerrar un proyecto, a fin de cumplir con los objetivos de rendimiento definidos en el plan de
                gestión del proyecto.</li>
            <li>
                <strong>Control Integrado de Cambios:</strong>
            revisar todas las solicitudes de cambio, aprobar los cambios, y controlar los cambios en los productos entregables y en los
            activos de los procesos de la organización.
            </li>
            <li>
                <strong>
            Cerrar Proyecto:
                </strong>
            finalizar todas las actividades en todos los Grupos de Procesos de Dirección de Proyectos para cerrar formalmente el proyecto
            o una fase del proyecto.
            </li>

            </ul>
