<h3>Definición del PMBOK</h3>
            <p class ="lead">El PMBOK es una guía que contiene los fundamentos para la efectiva dirección de proyectos. Está compuesta por una colección
                de procesos y áreas de conocimiento generalmente aceptadas como las mejores prácticas para su desarrollo. Esta guía fue
                propuesta por el PMI, la cual es una asociación de profesionales que practican la gerencia de proyectos.</p>
            <p class ="lead">Así también, esta guía proporciona un vocabularios en común para que los involucrados puedan analizar, escribir y aplicar
                la dirección de proyectos</p>
            <h3>Definición de Proyecto</h3>
            <p class ="lead">Un proyecto es un esfuerzo temporal que se lleva a cabo para crear un producto, servicio o resultado único.</p>
            <p class ="lead">En primer lugar, el proyecto es temporal ya que cada proyecto tiene un comienzo definido y un final definido. El final se
                alcanza cuando se han logrado los objetivos del proyecto o cuando queda claro que los objetivos del proyecto no serán
                o no podrán ser alcanzados, o cuando la necesidad del proyecto ya no exista y el proyecto sea cancelado. Temporal no
                necesariamente significa de corta duración; muchos proyectos duran varios años. En cada caso, sin embargo, la duración
                de un proyecto es limitada. Los proyectos no son esfuerzos continuos.</p>
            <p class ="lead">La naturaleza temporal de los proyectos puede aplicarse también a otros aspectos de la empresa. En segundo lugar, el proyecto
                crea productos o servicios entregables únicos. Según las normas publicadas en el PMBOK, “los proyectos pueden crear:</p>
                
                <p class ="lead">Un producto o artículo producido, que es cuantificable, y que puede ser un elemento terminado o un componente. La capacidad
                de prestar un servicio como, por ejemplo, las funciones del negocio que respaldan la producción o la distribución.</p>
               <p class ="lead">Un resultado como, por ejemplo, salidas o documentos. Por ejemplo, de un proyecto de investigación se obtienen conocimientos
                que pueden usarse para determinar si existe o no una tendencia o si un nuevo proceso beneficiará a la sociedad.</p>
                <p class ="lead">Los productos que se obtienen en cualquier proyecto, no necesariamente de sistemas, son por definición diferentes, esto se
                especifica en la característica de singularidad, la cual es una de las más importantes de cualquier proyecto. Como ejemplo
                tenemos a las personas, producto de la unión de un hombre y una mujer, las cuales son diferentes, aunque existan casos
            de gemelos o siameses, estos son diferentes en personalidad o alguna otra característica.</p>
