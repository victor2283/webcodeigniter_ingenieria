<h3>3- Gestión del Tiempo del Proyecto</h3>
<p class ="lead">Esta área define los pasos necesarios para que el proyecto se concluya en el tiempo establecido por el cliente. Según PMBOK,
los procesos de Gestión del Tiempo del Proyecto incluyen lo siguiente:</p>
<ul>
<li>Definición de las Actividades: identifica las actividades específicas del cronograma que deben ser realizadas para producir
los diferentes productos entregables del proyecto.
</li>
<li>Establecimiento de la Secuencia de las Actividades: identifica y documenta las dependencias entre las actividades del cronograma.</li>
<li>Estimación de Recursos de las Actividades: estima el tipo y las cantidades de recursos necesarios para realizar cada actividad
del cronograma.</li>
<li>Estimación de la Duración de las Actividades: estima la cantidad de períodos laborables que serán necesarios para completar
cada actividad del cronograma.</li>
<li>Desarrollo del Cronograma: analiza las secuencias de las actividades, la duración de las actividades, los requisitos de recursos
y las restricciones del cronograma para crear el cronograma del proyecto.</li>
<li>Control del Cronograma: controla los cambios del cronograma del proyecto.</li>
</ul>
<p class ="lead">Esta área del conocimiento es una de las más importantes de todas, ya que los procesos que define se relacionan con los de
las demás áreas del conocimiento. Estos procesos se presentan como componentes discretos con las interfaces del área
de conocimiento, pero en la realidad puede interactuar de maneras que no se especifican en el manual del PMBOK 
</p>
<p class ="lead">Cuando el proyecto es de bajo alcance, la definición del tiempo tiende a no ser tan exacta, debido a que existe una holgura
mayor, caso contrario, cuando un proyecto es de gran alcance, se debe hacer tener un mayor control al momento de definir
las labores de las personas.</p>
