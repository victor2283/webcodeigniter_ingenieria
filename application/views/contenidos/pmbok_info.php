            <h3>2- Gestión del Alcance del Proyecto</h3>
            <p class ="lead">
            Esta área de conocimiento define los procesos que permiten que el proyecto llegue a un final donde se alcance con las expectativas
            del cliente. Para esto se evalúa que requerimientos son los necesarios para que el trabajo concluya satisfactoriamente.
            </p>
            <p class ="lead">
            Esta área delimita hasta donde se quiere llegar en el desarrollo del proyecto, se define y se lleva el control de lo que
            está incluido en el proyecto. Los procesos que se definen en la guía PMBOK incluyen:
            </p>
            <ul>
            <li>
                <strong>Planificación del Alcance:</strong> crear un plan de gestión del alcance del proyecto que refleje cómo se definirá, verificará
                y
                controlará el alcance del proyecto, y cómo se creará y definirá la Estructura de Desglose del Trabajo (EDT).</li> 
            <li>
                <strong>Definición del Alcance:</strong> desarrollar un enunciado del alcance del proyecto detallado como base para futuras decisiones
                del
                proyecto.</li>
            <li>
                <strong>Crear EDT:</strong> subdividir los principales productos entregables del proyecto y el trabajo del proyecto en componentes
                más pequeños
                y más fáciles de manejar.</li>
            <li>
                <strong>Verificación del Alcance:</strong> formalizar la aceptación de los productos entregables completados
            del proyecto.</li>
            <li>
                <strong>Control del Alcance:</strong> controlar los cambios en el alcance del proyecto.</li>
            </ul>
<p class ="lead">Esta área del conocimiento es una de las más importantes de todas, ya que los procesos que define se relacionan con los de
las demás áreas del conocimiento. Estos procesos se presentan como componentes discretos con las interfaces del área
de conocimiento, pero en la realidad puede interactuar de maneras que no se especifican en el manual del PMBOK.
</p>
<p class ="lead">Según la guía definida por el PMI, “la palabra alcance puede referirse a lo siguiente:</p>
<ul>
<li>
<strong>Alcance del producto.</strong> Las características y funciones que caracterizan a un producto, servicio o resultado.
</li>
<li>
<strong>Alcance del proyecto.</strong> El trabajo que debe realizarse para entregar un producto, servicio o resultado con las
funciones y características
especificadas.</li>
</ul>
