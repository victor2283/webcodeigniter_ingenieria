<h1>OBJETIVOS DE LA METODOLOGIA EUP</h1>



<p class ="lead">Los Objetivo de Enterprise Business Modeling para una  organización 
    de TI es:</p>
<ul>
<li><p class ="lead">Ayuda a facilitar un entendimiento común del negocio en el que está involucrada su 
    organización. Aunque esto puede parecer superfluo para algunos, la documentación 
    formal del negocio en el que participa su organización conducirá a muchas conversaciones
     interesantes sobre cómo deberían funcionar las cosas y, en última instancia, a un 
     entendimiento compartido de exactamente en qué negocio está y cómo ejecuta su trabajo.</p></li>
<li>
<p class ="lead">Ayuda a identificar áreas de su negocio que pueden mejorarse a través de la 
    automatización dirigida o la reingeniería de procesos empresariales a gran escala.</p>
</li>
<li><p class ="lead">Ayuda a identificar las áreas de negocios que su organización aún no aborda o en las que
     su organización es muy débil.</p></li>
<li>
<p class ="lead">Proporciona información importante a los equipos de proyectos que pueden ayudarlos
     a delimitar el alcance de su proyecto, en particular, ayudándoles a identificar 
     cómo sus esfuerzos encajan en el negocio en general.</p>
</li>     
</ul>

    



<p class ="lead">El desarrollo del modelo de negocio empresarial comienza con una visión amplia de todo el 
    negocio. Esto no significa que modelará todo su negocio: por ejemplo, puede optar 
    por ignorar los aspectos contables de su organización porque es estable, pero sí
     significa que su modelo irá más allá del alcance de un solo proyecto. Su objetivo
      debe ser capturar los fundamentos de su negocio al trabajar con sus partes 
      interesadas en un modelo que abarque todo pero que sea superficial;
       No necesitas muchos detalles, pero sí necesitas amplitud. Debe identificar
        y priorizar las áreas de importancia para que puedan ser exploradas 
        en detalle por los esfuerzos de modelado de negocios de los equipos de 
        proyectos individuales. Su modelo de negocio empresarial debe definir 
        una precisa, Visión general de alto nivel del negocio y debe contener información
         que sea relativamente estable en el tiempo. Por ejemplo, dentro de un 
         banco, la necesidad de procesar transacciones de banca minorista ha existido 
         durante siglos, pero la forma en que esto ocurre ha cambiado dramáticamente
          con el tiempo: la necesidad de alto nivel es estable, mientras que los detalles
           son muy fluidos. Los esfuerzos de modelado de negocios de su empresa deben explorar
            una amplia gama de problemas y, como resultado, su modelo de negocios de 
            empresa en realidad se compone de una colección de artefactos más 
            pequeños, como:</P>

<ul>
<li>Reglas de negocio de la empresa</li>
<li>Modelo de proceso empresarial empresarial.</li>
<li>Modelo de dominio empresarial</li>
<li>Declaración de la misión de la empresa</li>
<li>Visión empresarial</li>
<li>Modelo de organizacion</li>
</ul>












