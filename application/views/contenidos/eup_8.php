     <h1>Comparación entre RUP y EUP</h1>
      <p class ="lead">De la descripción de los ciclos de vida del <FONT class ="text-danger" SIZE=4> RUP </FONT> y del <FONT class ="text-primary" SIZE=4>EUP</FONT> se puede
           ver que el <FONT class ="text-danger" SIZE=4>Enterprise Unified Process(EUP)</FONT> muestra una clara 
           inclinación a la empresa como entidad y no solo al producto 
           software. Por ejemplo, la primera disciplina del <strong class ="lead">RUP</strong> es 
           <strong>Business Modelling (BM)</strong> mientras que la del <strong>EUP</strong> es 
           <strong>Enterprise Business Modelling (EBM)</strong>. El <strong>BM</strong> se centra
           en el producto software y le interesa la información relacionada 
           a un área determinada; en cambio, el <strong>EBM</strong> tiene su foco en la empresa y
           le interesa la información de la empresa en general. Sin embargo, el 
           ciclo de vida de cualquier sistema debe comprender desde
           la idea de su formación hasta la inactividad del mismo. Como se aprecia
            en el gráfico siguiente, el ciclo de vida de un sistema
            abarca más puntos que sólo las actividades del desarrollo 
            del producto software.
        </p>
        <p class ="lead">El <FONT class ="text-danger" SIZE=4>RUP</FONT> cubre de forma acertada dichas actividades pero no va 
            más allá. Es decir, no abarca lo referido a la post-entrega.
            El <FONT class ="text-dark" SIZE=4>EUP</FONT> si lo hace por medio de sus fases de <strong>Producción y Retiro.</strong></p>
