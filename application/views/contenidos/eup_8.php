            <p class="lead">Existen 4 tipos de ciclos de vida marcados desde el punto de vista del 
                alcance de los mismos.</p>
            <ul>
                <li><strong>Ciclo de vida del Desarrollo del Sistema</strong>, abarca exclusivamente las 
                    actividades del desarrollo del software. El RUP cubre
                cabalmente este ciclo, es decir, pertenece a él.</li>
                <li><strong>Ciclo de vida del Sistema</strong>, se basa en el anterior y añade las 
                    actividades para operar y dar soporte al sistema, una vez puesto
                en producción o cuando pase al retiro.</li>
                <li><strong>Ciclo de vida de la Tecnología de Información (IT)</strong>, abarca las 
                    actividades del departamento de tecnologías de información
                y las que sirven para realizar un adecuado manejo de la organización. 
                El EUP pertenece a este tipo de ciclo.</li>
                <li><strong>Ciclo de vida de la Organización o del Negocio</strong>, comprende el 
                    ciclo anterior y lo fusiona con las actividades del negocio.</li>
              </ul>
            <p class ="lead">De esta agrupación de ciclos de vida se desprende que el RUP es
                   limitado en lo referido a las actividades del negocio y sólo
            se concentra en las relacionadas al desarrollo del producto software.
            </p>
            <p class ="lead">Esto ya no es suficiente, se debe ofrecer más que un producto software. 
                El proceso no debe acabar en la entrega del aplicativo
                a la empresa que lo requirió, sino debe velar que éste funcione 
                adecuadamente luego de que se haya puesto en producción.
                Por eso es indispensable escalar el RUP, y así llegar al EUP, 
                que abarca la producción, el retiro y, para ello, realiza
                actividades del Gestión de la cartera, Arquitectura empresarial y 
                Reutilización estratégica.
            </p>

            <hr>
            <h2 class ="text-danger">Fase de Producción</h2>
            <p class ="lead">Es la primera fase adicional del EUP respecto del RUP. El ciclo de vida 
                del Rational Unified Process concluía con la fase
                de Transición, pero se ha visto que realmente no debe acabar allí. 
                Las actividades propias de esta fase están más relacionadas
                a las tecnologías de información (IT) que a la construcción y al 
                despliegue del software, ya que se da cuando esto ya ha
                terminado
            </p>
            <p class ="lead">Esta fase del EUP contiene las actividades de soporte y operación del 
                sistema, para ello el aplicativo debe estar en funcionamiento.
               Estas actividades, que forman parte del ciclo de vida del sistema, 
               duran hasta que se decida reemplazar el aplicativo,
               ya sea por una nueva versión o por otro software. También la causa se 
               puede deber a que dicho aplicativo ya no estará
               más en uso, es decir, pasará al retiro.
            </p>
            <p class ="lead">Pero se pueden presentar nuevos requerimientos por parte de los usuarios 
                finales o cambios en la administración de los defectos
               encontrados. En este caso, se deberá verificar que dichos cambios pasen 
               a formar parte del nuevo ciclo de vida del desarrollo
               del sistema.
            </p>
            <h4 class ="text-danger"> Objetivos</h4>
            <p class ="lead">El objetivo principal de esta fase es mantener el sistema útil y 
                productivo para la organización que adquirió el producto
                software. Para cumplir con estos niveles de servicio, especificados 
                entre el usuario y el personal de soporte, es necesario
                realizar planes de acción, en casos extremos, para dar mantenimiento 
                de emergencia al sistema. Para mantener los sistemas
                operando correctamente, se debe brindar diferentes soluciones. Estas 
                soluciones dependen del giro de la organización
                y del software que cuentan. En algunos casos, se brindarán soporte 
                en las operaciones o cambios en ciertas funcionalidades
                y, en otros, solo se hará soporte del tipo help desk, por ejemplo.
            </p>
            
