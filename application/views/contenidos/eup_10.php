<h1>VENTAS Y DESVENTAJAS</h1>
<div class ="lead">
<p class ="lead">El desarrollo ágil de software es un enfoque que se utiliza para diseñar 
    un proceso disciplinado de administración de software que también permite 
    algunas modificaciones frecuentes en el proyecto de desarrollo. Este es un tipo
     de metodologías de desarrollo de software que es un marco conceptual para llevar
      a cabo varios proyectos de ingeniería de software. Se utiliza para minimizar el 
      riesgo mediante el desarrollo de software en cajas de tiempo corto que se llaman 
      iteraciones que generalmente duran de una semana a un mes.</p>
<h2>Ventajas de la metodología de desarrollo ágil:</h2>
<ul>
<li>La metodología ágil tiene un enfoque adaptativo que es capaz de responder a los requisitos cambiantes de los clientes.</li>
<li>La comunicación directa y los comentarios constantes del representante del cliente no dejan espacio para ninguna conjetura en el sistema</li>
</ul>

<h2>Desventajas de la metodología de desarrollo ágil:</h2>
<ul>
<li>Esta metodología se centra en el software de trabajo en lugar de la documentación, por lo que puede resultar en una falta de documentación</li>
<li>El proyecto de desarrollo de software puede desviarse si el cliente no tiene muy claro el resultado final de su proyecto</li>
</ul>
</div>