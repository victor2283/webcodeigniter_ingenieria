    <div class ="col-lg-9">
        <p class ="lead">Larry LeRoy Constantine (pronunciado Constanteen ; nacido en 1943) es un ingeniero de software estadounidense , profesor en el Centro de Ciencias Exactas e Ingeniería de la Universidad de Madeira Portugal , y considerado uno de los pioneros de la computación . [1] Ha contribuido con numerosos conceptos y técnicas que forman las bases de la práctica moderna en ingeniería de software y diseño y desarrollo de aplicaciones. </p>
        <p class ="lead">Constantine creció en Anoka, Minnesota , y se graduó de Anoka High School en 1961 después de haber participado activamente en el debate y en otras actividades extra curriculares. Sus compañeros de clase lo nombraron "el más probable de tener éxito". Constantine recibió una SB en Administración de MIT Sloan School of Management en 1967 con una especialización en sistemas de información y psicología. Recibió un certificado en Family Therapy 1973 del Boston Family Institute, un programa de capacitación de postgrado de dos años. </p>
        <p class ="lead">Constantino comenzó su carrera laboral como Técnico / Ayudante Técnico en el Laboratorio de Ciencias Nucleares del MIT en 1963. De 1963 a 1966 fue Consultor de Personal y Programador / Analista en CEIR, Inc. Desde 1966 a 1968 fue Presidente de Información y Sistemas. Institute, Inc. En 1967, también se convirtió en instructor del programa de postgrado en la Wharton School of Business de la Universidad de Pennsylvania. De 1968 a 1972 fue miembro de la facultad del IBM Systems Research Institute. </p>
        <p class ="lead">En 1973 se convirtió en Director de Investigación, Concord, Massachusetts Family Service Society. De 1973 a 1980 fue Profesor Clínico Asistente de Psiquiatría, Universidad de Tufts , Escuela de Medicina. Hasta 1987 fue profesor asistente de desarrollo humano y estudios familiares (adjunto) en la Universidad de Connecticut . De 1984 a 1986 fue también Supervisor Clínico, Intervención de Adolescentes y Familias, LUK, Inc., Fitchburg, Massachusetts. De 1987 a 1993, también trabajó como Consultor Independiente. Se mantuvo como científico jefe, consultor principal, Constantine & Lockwood, Ltd desde 1993. Desde 1994 a 1999 fue profesor de tecnología de la información en la Universidad de Tecnología de Sydney , Australia. [3]Desde 2006, ha sido profesor en el Departamento de Matemáticas e Ingeniería de la Universidad de Madeira Portugal , donde dirigió el Laboratorio de Ingeniería de Software Centrado en el Uso (LabUSE), un antiguo centro de investigación dedicado a estudiar los aspectos humanos de la ingeniería de software moderna antes de convertirse en Instituto Fellow en el Madeira Interactive Technologies Institute en 2010.</p>
        <p class ="lead">En 1999, Constantine recibió el Premio Jolt a la Excelencia de Producto, el mejor libro de 1999 por su "Software para uso". En 2001 recibió el Premio a la Excelencia Platinum (primer lugar), Concurso de diseño centrado en el rendimiento 2001: Siemens AG, STEP-7 Lite. En 2006 fue reconocido como Ingeniero Distinguido por la Asociación de Maquinaria de Computación , y en 2007 fue nombrado Miembro de la ACM. Es el ganador del Premio Stevens en 2009 por "contribuciones destacadas a la literatura o la práctica de métodos para el desarrollo de software y sistemas". Recibió un Premio Simon Rockower en 2011 de la American Jewish Press Association . [5] Fue finalista para el 2014.Premio al libro Eric Hoffer . [6] Es miembro profesional de la Industrial Designers Society of America , miembro de la Asociación de Profesionales de la Usabilidad y de la IEEE Computer Society , y es miembro activo de los Escritores de ciencia ficción y fantasía de América .</p>
