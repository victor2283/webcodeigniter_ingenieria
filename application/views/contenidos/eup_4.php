<h1>Larry LeRoy Constantine</h1>

<div class ="row"> 
    <div class ="col-lg-3">
        <img src="assets/img/autores_eup/Larry_Constantine.jpg" alt="Raised Image" class="img-raised rounded img-fluid"> 
