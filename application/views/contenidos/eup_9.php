<h3 class="text-danger">Roles</h3>
            <p class ="lead">
                El EUP define una serie de roles relacionados a las actividades de 
                producción. Estos roles abarcan todos las funciones que
                se deben considerar para que se brinde servicios de operación y soporte 
                de manera eficiente y efectiva. Los roles que se
                usan para la disciplina de producción se detallarán a continuación.
            </P>
            <ul>
                <li>
                    <strong>Operador.</strong> Monitorear el sistema para verificar que 
                    opere correctamente, para comenzar 
                        los trabajos y procesos adecuados,
                     para realizar un backup y restaurar la data y para ocuparse de, 
                     de forma general, del sistema y su entorno.
                </li>
                <li>
                    <strong>Gerente de Soporte</strong>. Programa y asigna tareas. Debe 
                    asegurarse que el personal cuente con recursos necesarios para poder
                    realizar las tareas y, con esto, que el trabajo esté terminado a tiempo
                    y de forma óptima.
                 </li>
                 <li>
                    <strong>Representantes de apoyo de clientes o usuarios finales:</strong> 
                      <p class ="lead">Deben proporcionar  guías a los usuarios y recibir los reportes de 
                         las fallas ocurridas. Están en permanente contacto con el usuario y, si
                          fuera el caso, muestran el progreso de la solución del problema reportado.</p> 
                 </li>
                 <li>
                     <strong>Representantes de apoyo de sistema</strong> . Son los representantes del 
                     soporte al sistema. Reciben los informes de los problemas reportados
                     de los Representantes de atención al cliente. Si estos problemas son originados 
                     por falla del sistema deben crear defectos
                     para que sean atendidos por los Desarrolladores de soporte.
                 </li>
                    <li>
                        <strong>Desarrollador de soporte</strong>. Son los encargados de realizar los
                         cambios en el sistema con 
                        el fin de que vuelva a funcionar
                        correctamente.
                    </li>
            </ul>
