<h1>VISION GENERAL DE LA METODOLOGIA (EUP) extensiones RUP desde 2002</h1>
        <p class ="lead">Enterprise Unified Process (EUP) es una variante extendida del Proceso Unificado (RUP) y
         fue desarrollado por <a href="eup_4"> Scott W. Ambler y Larry Constantine en 2000, reelaborado en 2005 
         por Ambler, John Nalbone y Michael Vizdos</a>. EUP fue introducido originalmente
         para superar algunas carencias de RUP, a saber, la falta de rendimiento y utilidad así como 
         la eventual retirada de un sistema de software. Se agregaron dos fases y varias 
         disciplinas nuevas. EUP considera que el desarrollo de software
         no es una actividad autónoma, sino que está integrado en el ciclo de vida del sistema
          (que se construirá o mejorará o sustituirá), el ciclo de vida de TI de la empresa 
          y el ciclo de vida de la empresa. Se trata de desarrollo de software
            como se ve desde el punto de vista del cliente.
        </p>
