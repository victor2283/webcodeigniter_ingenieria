<h2 class="text-danger" id="">Tipos de Kanban</h2>
<hr/>
<p class ="lead">
    De acuerdo al modelo Kanban empleado por Toyota, existen básicamente dos tipos de tarjetas Kanban, estas son:
</p>

<ul>
    <li>Kanban de retiro.
    </li>

    <li>Kanban de producción.
    </li>
</ul>
<h3 class="text-danger">KANBAN DE RETIRO</h3>
<p class ="lead">
    Un kanban o tarjeta de retiro especifica la referencia y la cantidad de producto que un proceso debe retirar del proceso inmediatamente anterior, o de su contenedor de producto (pequeños
    almacenes reguladores entre procesos).
</p>
    <figure class="cc-imagewrapper cc-m-image-align-3">
    <img srcset="https://image.jimcdn.com/app/cms/image/transf/dimension=320x10000:format=png/path/s075f076504dfea8d/image/i7a3d8f08fcfca29a/version/1482690050/image.png 320w, https://image.jimcdn.com/app/cms/image/transf/none/path/s075f076504dfea8d/image/i7a3d8f08fcfca29a/version/1482690050/image.png 456w" sizes="(min-width: 456px) 456px, 100vw" id="cc-m-imagesubtitle-image-5853910413" src="https://image.jimcdn.com/app/cms/image/transf/none/path/s075f076504dfea8d/image/i7a3d8f08fcfca29a/version/1482690050/image.png" alt="" class ="text-danger" data-src-width="456" data-src-height="246" data-src="https://image.jimcdn.com/app/cms/image/transf/none/path/s075f076504dfea8d/image/i7a3d8f08fcfca29a/version/1482690050/image.png" data-image-id="3977980013"/>    
    </figure>

<h3 class ="text-danger" >KANBAN DE PRODUCCIÓN</h3>
<p class ="lead">
    Un kanban o tarjeta de producción especifica la referencia y la cantidad de producto que un proceso debe producir.
</p>
<figure class="cc-imagewrapper cc-m-image-align-3">
<img srcset="https://image.jimcdn.com/app/cms/image/transf/dimension=320x10000:format=png/path/s075f076504dfea8d/image/i2d6dbf5600296811/version/1482690530/image.png 320w, https://image.jimcdn.com/app/cms/image/transf/none/path/s075f076504dfea8d/image/i2d6dbf5600296811/version/1482690530/image.png 456w" sizes="(min-width: 456px) 456px, 100vw" id="cc-m-imagesubtitle-image-5853910813" src="https://image.jimcdn.com/app/cms/image/transf/none/path/s075f076504dfea8d/image/i2d6dbf5600296811/version/1482690530/image.png" alt="" class ="text-danger" data-src-width="456" data-src-height="205" data-src="https://image.jimcdn.com/app/cms/image/transf/none/path/s075f076504dfea8d/image/i2d6dbf5600296811/version/1482690530/image.png" data-image-id="3977980113"/>    
</figure>
<hr/>
<p class ="lead">
    El funcionamiento del sistema Kanban es relativamente sencillo. Teniendo en cuenta el modelo original de Toyota, el sistema de entrada consta de un tablero en el que depositamos las tarjetas
    (señales), el tablero se sitúa de manera que el operario lo pueda ver con facilidad desde su posición normal o habitual. Cada tarjeta está asociada a un contenedor o unidad de almacenamiento. En
    caso de que el contenedor esté vacío, la tarjeta deberá estar en el tablero, si en caso contrario, está lleno, la tarjeta deberá acompañar al contenedor.
</p>


<p class ="lead">
    Así entonces, en caso de que el tablero se encuentre lleno de tarjetas, quiere decir que no quedan piezas en inventario y es importante producir unidades (zona roja del tablero). Si las tarjetas
    están en la zona amarilla o verde del tablero, significa que quedan unidades en inventario y que probablemente no sea necesario producir.
</p>


<p class ="lead">
    De manera que si el proceso proveedor inicia la producción, toma la tarjeta del tablero y la coloca en el contenedor en el que irá depositando las unidades correspondientes al lote. 
</p>
<p class ="lead">
    Una vez que finaliza, ubica el contenedor en el almacén intermedio.
</p>
<p class ="lead">
    Acto seguido, el proceso cliente comienza a consumir las piezas depositadas en el contenedor del almacén intermedio; una vez consume todas las unidades del contenedor, ubicará la tarjeta que
    acompaña al mismo, en el tablero de tarjetas, y devuelve el contenedor totalmente vacío.
</p>

<p class ="lead">
    Debe considerarse que la cantidad de tarjetas y contenedores entre procesos no se definen de manera arbitraria, sino que se determinan en función de los parámetros del sistema de producción, tema
    que abordaremos más adelante.
</p>


        <figure class="cc-imagewrapper cc-m-image-align-3">
