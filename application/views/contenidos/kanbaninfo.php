        </figure>

        
    

        <p class ="lead">
            Para adoptar kanban debe considerarse que la producción debe ser nivelada y mezclada, de manera que casi siempre se deben fabricar los mismos volúmenes. Este sistema no permite variabilidades de
            más del 15%-20% sin cambiar los parámetros de señalización. En las líneas de producción se  fabrica con un mix de productos, de manera que pueden utilizarse diferentes rangos de
            colores por referencia en un mismo tablero, así el operario sabrá que referencia deberá fabricar en cada momento.
        </p>
    
    


