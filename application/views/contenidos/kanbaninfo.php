        <h2 class="text-danger" >¿Qué es Kanban?</h2>
    
    
 <hr/>


 <p class ="lead">
    
    <em><strong>Kanban</strong></em> es una palabra de origen japonés que significa <em><strong>tarjeta, </strong></em>su concepto ha evolucionado hasta convertirse en
    <em><strong>señal, </strong></em>y se puede definir como un sistema de flujo que permite, mediante el uso de señales, la movilización de unidades a través de una línea de producción
    mediante una <em><strong>estrategia pull o estrategia de jalonamiento</strong></em>.
</p>
<hr/>
    <h3 class="text-danger" >¿Qué es un sistema pull?</h3><hr/>
    <figure class="cc-imagewrapper cc-m-image-align-1">

        <img srcset="https://image.jimcdn.com/app/cms/image/transf/none/path/s075f076504dfea8d/image/if8bc595e7c7b8a8b/version/1482687041/image.png 142w" sizes="(min-width: 142px) 142px, 100vw" id="cc-m-textwithimage-image-5853905013" src="https://image.jimcdn.com/app/cms/image/transf/none/path/s075f076504dfea8d/image/if8bc595e7c7b8a8b/version/1482687041/image.png" alt="" class ="text-danger" data-src-width="142" data-src-height="146" data-src="https://image.jimcdn.com/app/cms/image/transf/none/path/s075f076504dfea8d/image/if8bc595e7c7b8a8b/version/1482687041/image.png" data-image-id="3977976013"/>    

    </figure>

<p class ="lead">
    Un sistema de flujo pull consiste en optimizar los inventarios y el flujo del producto de acuerdo al comportamiento real de la demanda.
</p>


<p class ="lead">
    En estos sistemas el proceso logístico inicia con el pedido del cliente, y aunque sea el sistema ideal por optimización de inventarios, la apuesta por conocer la demanda en tiempo real y
    flexibilizar la cadena para responder a sus necesidades es una apuesta compleja. Sin embargo al igual que la mayoría de las prácticas logísticas de vanguardia, gran número de casos de éxito se
    fundamentan en la aplicación de un sistema de flujo pull.
</p>


<p class ="lead">
    De igual manera, el sistema de flujo pull se aplica a la líneas de producción, en cuyo caso práctico, los clientes son procesos previos (clientes internos), y la herramienta por excelencia que
    permite conocer la demanda en tiempo real y flexibilizar la línea de producción es <em><strong>Kanban.</strong></em>
</p>    




<p class ="lead">
    Así entonces, podemos concluir que Kanban jalona el inventario a través de centros de trabajo, utilizando tarjetas para señalar la necesidad de otro contenedor de material. O dicho de otra forma,
    la tarjeta es la autorización para que se produzca el siguiente lote de producción.
</p>
    
    
    


