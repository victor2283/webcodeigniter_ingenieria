    <h2 class ="text-danger" id="cc-m-header-5853924713">¿Cómo implementar Kanban?</h2>

    <hr/>
<p class ="lead">
    De acuerdo a la implementación del modelo Kanban en Toyota, el procedimiento para implementar Kanban es sistemático y contempla los siguientes pasos, en los cuales se definen los parámetros del
    sistema de producción para adoptar la herramienta:
</p>

<ol>
    <li>Seleccionar las referencias que se van a producir mediante Kanban.
    </li>

    <li>Calcular la cantidad de piezas por kanban (tamaño del lote).
    </li>

    <li style="text-align: justify;">Escoger el tipo de señal y el tipo de contenedor estándar. El contenedor puede variar por referencia.
    </li>

    <li style="text-align: justify;">Calcular el número de contenedores por referencia (curva de producción) y la secuencia pitch.
    </li>

    <li style="text-align: justify;">Dar seguiemiento (WIP o SWIP).
    </li>
</ol>
    <h3 class ="text-danger" id="cc-m-header-5853925213">SELECCIONAR LAS REFERENCIAS</h3>

<p class ="lead">
    Las referencias deben elegirse de acuerdo a las familias de productos existentes, además se recomienda utilizar grupos en los cuales exista un trabajo previo de mejora, como por ejemplo SMED,
    celdas de manufactura o balances de líneas.
</p>
    <h3 class ="text-danger" id="cc-m-header-5853926713">CALCULAR LA CANTIDAD DE PIEZAS POR KANBAN</h3>

    <p class ="lead">
        Para determinar la cantidad de piezas por kanban, también conocido como Inventario Total Requerido (ITR), se aplica la siguiente fórmula:
    </p>


<p style="text-align: center;">
    <em>Piezas por Kanban (ITR) = D x TE x U x %VD</em>
</p>


<p class ="lead">
    <em>Donde, </em>
</p>

<p class ="lead">
    <em>D = </em>Demanda por horizonte de tiempo (por ejemplo y habitualmente semanas).
</p>

<p class ="lead">
    <em>TE</em> = Tiempo de entrega en las mismas unidades del horizonte de la demanda.
</p>

<p class ="lead">
    <em>U</em> = Número de ubicaciones (almacenes intermedios).
</p>

<p class ="lead">
    <em>%VD</em> = Nivel de variación de la demanda. Se obtiene mediante la desviación estándar de la demanda sobre el promedio de la demanda.
</p>

<p class ="lead">
    Por ejemplo:
</p>
<p class ="lead">
    Se desea implementar Kanban entre los procesos A y B de un sistema de producción. Los requerimientos de materiales del proceso B en el último año son los siguientes:
</p>

    <figure class="cc-imagewrapper cc-m-image-align-3">
    <img srcset="https://image.jimcdn.com/app/cms/image/transf/none/path/s075f076504dfea8d/image/idc1245dd9c5f541a/version/1482701050/image.png 173w" sizes="(min-width: 173px) 173px, 100vw" id="cc-m-imagesubtitle-image-5853927613" src="https://image.jimcdn.com/app/cms/image/transf/none/path/s075f076504dfea8d/image/idc1245dd9c5f541a/version/1482701050/image.png" alt="" class ="text-danger" data-src-width="173" data-src-height="277" data-src="https://image.jimcdn.com/app/cms/image/transf/none/path/s075f076504dfea8d/image/idc1245dd9c5f541a/version/1482701050/image.png" data-image-id="3977988113"/>    
    </figure>



<p class ="lead">
    <strong>El primer paso</strong> consiste en determinar la demanda semanal del proceso B, para ello acudimos al promedio del último año:
</p>


<p class ="lead">
    Promedio mensual = 1542 piezas
</p>

<p class ="lead">
    Promedio semanal = (1542 x 12 meses) / 52 semanas = 356 piezas semanales
</p>

<p class ="lead">
    Demanda = 356 piezas a la semana
</p>

<p class ="lead">
    <strong>El segundo paso</strong> consiste en determinar el tiempo de entrega (TE) de la demanda semanal, es decir de las 356 piezas. Para ello debemos considerar los tiempos de procesamiento,
    inspección, recepción, alistamiento. Asumamos que el TE de las 356 piezas es equivalente a 1 semana.
</p>


<p class ="lead">
    Tiempo de entrega = 1 semana
</p>


<p class ="lead">
    <strong>El tercer paso</strong> consiste en determinar el número de ubicaciones de los almacenes de producto en proceso, cuando se está iniciando la implementación es común utilizar dos
    ubicaciones, hasta reducirlo a una sola ubicación. Asumamos que se utilizará una sola ubicación.
</p>


<p class ="lead">
    Ubicaciones = 1
</p>


<p class ="lead">
    <strong>El cuarto paso</strong> consiste en determinar la variación de la demanda, para ello primero calculamos la desviación estándar de la demanda y luego lo dividimos entre el promedio:
</p>
<figure class="cc-imagewrapper cc-m-image-align-3">
<img srcset="https://image.jimcdn.com/app/cms/image/transf/dimension=320x10000:format=png/path/s075f076504dfea8d/image/ifb0e80698fefa6db/version/1482702213/image.png 320w, https://image.jimcdn.com/app/cms/image/transf/none/path/s075f076504dfea8d/image/ifb0e80698fefa6db/version/1482702213/image.png 510w" sizes="(min-width: 510px) 510px, 100vw" id="cc-m-imagesubtitle-image-5853928213" src="https://image.jimcdn.com/app/cms/image/transf/none/path/s075f076504dfea8d/image/ifb0e80698fefa6db/version/1482702213/image.png" alt="" class ="text-danger" data-src-width="510" data-src-height="352" data-src="https://image.jimcdn.com/app/cms/image/transf/none/path/s075f076504dfea8d/image/ifb0e80698fefa6db/version/1482702213/image.png" data-image-id="3977988313"/>    

</figure>


    <p class ="lead">
         %VD = 1 + (266 / 1542) = 1,1725
    </p>


<p class ="lead">
    Ahora ya podemos determinar el número de piezas por Kanban, o el Inventario total requerido (ITR) utilizando la fórmula inicial:
</p>

<p style="text-align: center;">
    <em>Piezas por Kanban (ITR) = 356 x 1 x 1 x 1,1725</em>
</p>

<p style="text-align: center;">
    <em>Piezas por Kanban (ITR) = 417,4 = 418</em>
</p>

