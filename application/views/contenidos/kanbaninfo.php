        <h3 class ="text-danger" id="">ESCOGER EL TIPO DE SEÑAL Y EL TIPO DE CONTENEDOR ESTÁNDAR</h3>
    
    
    <p class ="lead">
        Existen una serie de factores que deberán considerarse al momento de seleccionar el contenedor de cada referencia, algunos de ellos son:
    </p>

<ul>
    <li>Tamaño de las unidades.
    </li>

    <li>Peso del contenedor lleno.
    </li>

    <li>Capacidad de carga de los operarios.
    </li>

    <li>Equipos de manutención disponibles.
    </li>

    <li>Espacio disponible.
    </li>

    <li>Ergonomía del contenedor.
    </li>
</ul>
<h3 class ="text-danger" id="cc-m-header-5853929413">CALCULAR EL NÚMERO DE CONTENEDORES</h3>
<p class ="lead">
    La fórmula para determinar el número de contenedores es muy sencilla, para ello es preciso conocer la capacidad de los contenedores:
</p>

<p class ="lead">
    Número de contenedores = (Inventario Total Requerido / Capacidad del contenedor)
</p>


<p class ="lead">
    Para efectos del ejemplo que venimos manejando, supongamos que el tamaño del contenedor es de 20 piezas, así entonces:
</p>

<p class ="lead">
    Número de contendores = (418 / 20)
</p>

<p class ="lead">
    Número de contenedores = 21
</p>
<h3 class ="text-danger" id="">SEGUIMIENTO AL WIP VS. SWIP</h3>
<p class ="lead">
    WIP = Inventario de producto en proceso, es decir el inventario total en la celda.
</p>

<p class ="lead">
    SWIP = Inventario estándar de la celda.
</p>

<p class ="lead">
    WIP to SWIP = WIP / SWIP
</p>


<p class ="lead">
    El resultado ideal es equivalente a 1, es decir que el WIP sea igual al SWIP.
</p>


<p class ="lead">
    En los casos en los cuales sea mayor que 1, significa que existe mucho inventario en la celda, en caso de que sea menor que 1, significa que existe el riesgo de que la celda se quede
    desabastecida de materiales. 
</p>

