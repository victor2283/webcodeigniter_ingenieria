<h2 class ="text-danger" id="">Principios del Kanban</h2>
    <figure class="cc-imagewrapper cc-m-image-align-1">
        <img srcset="https://image.jimcdn.com/app/cms/image/transf/none/path/s075f076504dfea8d/image/i025503bcb8dcfce4/version/1482705242/image.png 119w" sizes="(min-width: 119px) 119px, 100vw" id="cc-m-textwithimage-image-5853930713" src="https://image.jimcdn.com/app/cms/image/transf/none/path/s075f076504dfea8d/image/i025503bcb8dcfce4/version/1482705242/image.png" alt="" class ="text-danger" data-src-width="119" data-src-height="97" data-src="https://image.jimcdn.com/app/cms/image/transf/none/path/s075f076504dfea8d/image/i025503bcb8dcfce4/version/1482705242/image.png" data-image-id="3977988413"/>    
    </figure>
<p class ="lead">
    1. No se pasan productos no conformes al siguiente proceso.
</p>


<p class ="lead">
    2. Se retira un kanban cuando un proceso retira unidades del proceso inmediatamente anterior.
</p>


<p class ="lead">
    3. El kanban es una orden de producción para los procesos proveedores.
</p>


<p class ="lead">
    4. Nada se produce o nada se transporte sin kanban.
</p>


<p class ="lead">
    5. El número de kanbans debe disminuir con el tiempo.
</p>    

<p class ="lead">        
    Si bien muchos especialistas coinciden en que Kanban es más un sistema de programación visual, un complemento <a href="/herramientas-para-el-ingeniero-industrial/lean-manufacturing/andon-control-visual/" title="Andon: Control visual"><em><strong>Andon</strong></em></a>; lo realmente cierto es que Kanban no es un sistema donde todo se pone en una programación, vale la pena considerar que
    Kanban es un sistema de control de producción diseñado para permitir que el encargado del proceso visualice los requerimientos de producción de una forma flexible y rápida, al mismo tiempo
    que se asegure de que todas las piezas o suministros son ordenados o producidos solo si es necesario.
</p>
            
